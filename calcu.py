num1 = int(input("Enter value1 :"))
num2 = int(input("Enter value2 :"))
opr = input("Enter opr... :")
if opr == "+":
      print(num1 + num2)

elif opr == "-":
      print(num1 - num2)

elif opr == "*":
      print(num1 * num2)

elif opr == "/":
      print(num1 / num2)
      
else:
    print("Invalid opr")


    
